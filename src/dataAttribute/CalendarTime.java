package dataAttribute;

public class CalendarTime {

    private Integer occ = 0;
    private String occType = "";
    private String occPer = "";
    private String weekDay = "";
    private String month = "";
    private Integer day = 0;
    private Integer hr = 0;
    private Integer mn = 0;

    public Integer getOcc() {
        return occ;
    }

    public void setOcc(Integer occ) {
        this.occ = occ;
    }

    public String getOccType() {
        return occType;
    }

    public void setOccType(String occType) {
        this.occType = occType;
    }

    public String getOccPer() {
        return occPer;
    }

    public void setOccPer(String occPer) {
        this.occPer = occPer;
    }

    public String getWeekDay() {
        return weekDay;
    }

    public void setWeekDay(String weekDay) {
        this.weekDay = weekDay;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public Integer getDay() {
        return day;
    }

    public void setDay(Integer day) {
        this.day = day;
    }

    public Integer getHr() {
        return hr;
    }

    public void setHr(Integer hr) {
        this.hr = hr;
    }

    public Integer getMn() {
        return mn;
    }

    public void setMn(Integer mn) {
        this.mn = mn;
    }
}
