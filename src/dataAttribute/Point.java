package dataAttribute;

public class Point {

    private Float xVal = 0.0F;
    private Float yVal = 0.0F;

    public Float getxVal() {
        return xVal;
    }

    public void setxVal(Float xVal) {
        this.xVal = xVal;
    }

    public Float getyVal() {
        return yVal;
    }

    public void setyVal(Float yVal) {
        this.yVal = yVal;
    }
}
